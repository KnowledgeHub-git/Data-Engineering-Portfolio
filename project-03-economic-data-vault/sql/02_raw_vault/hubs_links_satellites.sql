-- ===== HUBS (Business Keys) =====

CREATE OR REPLACE DYNAMIC TABLE ECON_VAULT.RAW_VAULT.HUB_INDICATOR
  TARGET_LAG = '1 hour' WAREHOUSE = COMPUTE_WH
AS
SELECT DISTINCT INDICATOR_HK, VARIABLE,
    MIN(LOAD_DTS) AS LOAD_DTS, MIN(RECORD_SOURCE) AS RECORD_SOURCE
FROM (
    SELECT INDICATOR_HK, VARIABLE, LOAD_DTS, RECORD_SOURCE FROM ECON_VAULT.STAGING.STG_FEDERAL_RESERVE
    UNION ALL SELECT INDICATOR_HK, VARIABLE, LOAD_DTS, RECORD_SOURCE FROM ECON_VAULT.STAGING.STG_BLS_EMPLOYMENT
    UNION ALL SELECT INDICATOR_HK, VARIABLE, LOAD_DTS, RECORD_SOURCE FROM ECON_VAULT.STAGING.STG_US_TREASURY
    UNION ALL SELECT INDICATOR_HK, VARIABLE, LOAD_DTS, RECORD_SOURCE FROM ECON_VAULT.STAGING.STG_WORLD_BANK
    UNION ALL SELECT INDICATOR_HK, VARIABLE, LOAD_DTS, RECORD_SOURCE FROM ECON_VAULT.STAGING.STG_VINTAGE
) GROUP BY INDICATOR_HK, VARIABLE;

